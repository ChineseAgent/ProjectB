class gebruiker
{
    string Gebruikersnaam;
    string Wachtwoord;
    string Email;
    int Telefoonnummer;

    public gebruiker(string gebruikersnaam, string wachtwoord, string email, int telefoonnummer)
    {
        this.Gebruikersnaam = gebruikersnaam;
        this.Wachtwoord = wachtwoord;
        this.Email = email;
        this.Telefoonnummer = telefoonnummer;
    }
}